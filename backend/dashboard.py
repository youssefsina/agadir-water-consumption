def style_ax(ax, title="", xlabel="", ylabel="", grid=True):
    ax.set_title(title, fontsize=11, fontweight="bold", pad=8, color=TEXT)
    ax.set_xlabel(xlabel, fontsize=8, color=SUBTEXT)
    ax.set_ylabel(ylabel, fontsize=8, color=SUBTEXT)
    if grid:
        ax.grid(True, alpha=0.4, linestyle="--")
    ax.spines[["top", "right"]].set_visible(False)
    ax.spines[["left", "bottom"]].set_color(GRID)


# PAGE 1 – Overview: one full day of raw sensor readings
def build_overview(df):
    fig = Figure(figsize=(14, 9), facecolor=BG, tight_layout=True)
    fig.suptitle("📊  Sensor Overview — First 7 Days", fontsize=14,
                 fontweight="bold", color=TEXT, y=0.98)

    sample = df.iloc[: 7 * 288]   # 7 days × 288 readings/day
    ts = sample.index

    sensors = [
        ("flow_lpm",          "Flow Rate (L/min)",     ACCENT,  "💧 Water Flow"),
        ("pressure_bar",      "Pressure (bar)",         BLUE,    "🔧 Pipeline Pressure"),
        ("soil_moisture_pct", "Soil Moisture (%)",      YELLOW,  "🌱 Soil Moisture"),
        ("temperature_c",     "Temperature (°C)",       WARN,    "🌡  Ambient Temperature"),
        ("rain_probability",  "Rain Probability",       ACCENT2, "🌧  Rain Forecast"),
    ]

    gs = gridspec.GridSpec(5, 1, figure=fig, hspace=0.55,
                           top=0.94, bottom=0.05, left=0.07, right=0.97)

    for i, (col, ylabel, color, title) in enumerate(sensors):
        ax = fig.add_subplot(gs[i])
        ax.fill_between(ts, sample[col], alpha=0.18, color=color)
        ax.plot(ts, sample[col], color=color, linewidth=0.9, label=ylabel)

        # shade irrigation windows
        irr = sample["is_irrigating"].values
        prev = 0
        for j, v in enumerate(irr):
            if v == 1 and prev == 0:
                start_ts = ts[j]
            if v == 0 and prev == 1:
                ax.axvspan(start_ts, ts[j], alpha=0.07, color=ACCENT, zorder=0)
            prev = v

        style_ax(ax, title=title, ylabel=ylabel)
        ax.tick_params(axis="x", labelsize=7)
        ax.tick_params(axis="y", labelsize=7)

    return fig


# PAGE 2 – Anomaly Timeline
def build_anomaly_timeline(df):
    fig = Figure(figsize=(14, 9), facecolor=BG, tight_layout=True)
    fig.suptitle("🚨  Anomaly Detection Timeline", fontsize=14,
                 fontweight="bold", color=TEXT, y=0.98)

    gs = gridspec.GridSpec(3, 1, figure=fig, hspace=0.55,
                           top=0.93, bottom=0.07, left=0.07, right=0.97)

    ax_flow = fig.add_subplot(gs[0])
    ax_pres = fig.add_subplot(gs[1])
    ax_label = fig.add_subplot(gs[2])

    # Downsample to keep rendering fast (every 3rd point)
    ds = df.iloc[::3]
    ts = ds.index

    # Flow
    ax_flow.plot(ts, ds["flow_lpm"], color=SUBTEXT, linewidth=0.5, alpha=0.6, zorder=1)
    for atype, color in ANOMALY_COLORS.items():
        if atype == "Normal":
            continue
        mask = ds["anomaly_type"] == atype
        if mask.any():
            ax_flow.scatter(ts[mask], ds["flow_lpm"][mask],
                            color=color, s=8, zorder=2, label=atype, alpha=0.85)
    style_ax(ax_flow, title="💧 Flow Rate with Anomalies", ylabel="L/min")
    ax_flow.legend(fontsize=7, ncol=3, loc="upper right",
                   framealpha=0.3, markerscale=2)

    # Pressure
    ax_pres.plot(ts, ds["pressure_bar"], color=SUBTEXT, linewidth=0.5, alpha=0.6, zorder=1)
    for atype, color in ANOMALY_COLORS.items():
        if atype == "Normal":
            continue
        mask = ds["anomaly_type"] == atype
        if mask.any():
            ax_pres.scatter(ts[mask], ds["pressure_bar"][mask],
                            color=color, s=8, zorder=2, alpha=0.85)
    style_ax(ax_pres, title="🔧 Pressure with Anomalies", ylabel="bar")

    # Anomaly label strip
    label_map_num = {
        "Normal": 0, "Night_Leak": 1, "Pipe_Burst": 2,
        "Over_Irrigation": 3, "Under_Irrigation": 4, "Rain_Event": 5
    }
    numeric = ds["anomaly_type"].map(label_map_num).fillna(0)
    for atype, color in ANOMALY_COLORS.items():
        if atype == "Normal":
            continue
        mask = ds["anomaly_type"] == atype
        if mask.any():
            ax_label.scatter(ts[mask], numeric[mask],
                             color=color, s=12, zorder=2, label=atype, alpha=0.9)

    ax_label.set_yticks(list(label_map_num.values()))
    ax_label.set_yticklabels(list(label_map_num.keys()), fontsize=7)
    style_ax(ax_label, title="🏷  Anomaly Label Strip", ylabel="")

    return fig


# PAGE 3 – Distribution & Statistics
def build_distributions(df):
    fig = Figure(figsize=(14, 9), facecolor=BG, tight_layout=True)
    fig.suptitle("📈  Sensor Distributions & Statistics", fontsize=14,
                 fontweight="bold", color=TEXT, y=0.98)

    gs = gridspec.GridSpec(2, 3, figure=fig, hspace=0.55, wspace=0.40,
                           top=0.93, bottom=0.07, left=0.07, right=0.97)

    sensor_cfg = [
        ("flow_lpm",          "Flow Rate (L/min)",   ACCENT),
        ("pressure_bar",      "Pressure (bar)",       BLUE),
        ("soil_moisture_pct", "Soil Moisture (%)",    YELLOW),
        ("temperature_c",     "Temperature (°C)",     WARN),
        ("rain_probability",  "Rain Probability",     ACCENT2),
    ]

    normal_df = df[df["anomaly_type"] == "Normal"]

    for i, (col, label, color) in enumerate(sensor_cfg):
        row, col_idx = divmod(i, 3)
        ax = fig.add_subplot(gs[row, col_idx])
        vals = normal_df[col].dropna()

        n_bins = 40
        counts, bins, patches = ax.hist(vals, bins=n_bins, color=color,
                                         alpha=0.75, edgecolor="none")
        ax.axvline(vals.mean(), color="white", linewidth=1.5,
                   linestyle="--", label=f"Mean: {vals.mean():.2f}")
        ax.axvline(vals.median(), color=YELLOW, linewidth=1.2,
                   linestyle=":", label=f"Median: {vals.median():.2f}")

        style_ax(ax, title=label, xlabel=label, ylabel="Count")
        ax.legend(fontsize=7, framealpha=0.3)

    # 6th panel – anomaly class balance (bar chart)
    ax6 = fig.add_subplot(gs[1, 2])
    counts = df["anomaly_type"].value_counts()
    bars = ax6.barh(counts.index, counts.values,
                    color=[ANOMALY_COLORS.get(t, SUBTEXT) for t in counts.index],
                    alpha=0.85, edgecolor="none", height=0.6)
    for bar, val in zip(bars, counts.values):
        ax6.text(val + 20, bar.get_y() + bar.get_height() / 2,
                 f"{val:,}", va="center", fontsize=7, color=TEXT)
    style_ax(ax6, title="🏷  Class Balance", xlabel="# Rows", grid=False)
    ax6.tick_params(axis="y", labelsize=7)

    return fig


# PAGE 4 – Correlation Heatmap + Scatter
def build_correlations(df):
    fig = Figure(figsize=(14, 9), facecolor=BG, tight_layout=True)
    fig.suptitle("🔗  Feature Correlations", fontsize=14,
                 fontweight="bold", color=TEXT, y=0.98)

    gs = gridspec.GridSpec(2, 2, figure=fig, hspace=0.50, wspace=0.35,
                           top=0.93, bottom=0.07, left=0.07, right=0.97)

    feature_cols = [
        "flow_lpm", "pressure_bar", "soil_moisture_pct",
        "temperature_c", "rain_probability",
        "flow_rolling_mean", "flow_rolling_std",
        "pressure_drop", "flow_deviation", "evap_index",
    ]
    corr = df[feature_cols].corr()

    # ── Heatmap (manual, no seaborn)
    ax_heat = fig.add_subplot(gs[0, :])
    im = ax_heat.imshow(corr.values, cmap="RdYlGn", vmin=-1, vmax=1, aspect="auto")
    fig.colorbar(im, ax=ax_heat, fraction=0.025, pad=0.02)
    ticks = range(len(feature_cols))
    ax_heat.set_xticks(ticks)
    ax_heat.set_yticks(ticks)
    ax_heat.set_xticklabels([c.replace("_", "\n") for c in feature_cols], fontsize=7)
    ax_heat.set_yticklabels(feature_cols, fontsize=7)
    for i in range(len(feature_cols)):
        for j in range(len(feature_cols)):
            val = corr.values[i, j]
            ax_heat.text(j, i, f"{val:.2f}", ha="center", va="center",
                         fontsize=6.5, color="black" if abs(val) > 0.5 else TEXT)
    ax_heat.set_title("Feature Correlation Matrix", fontsize=11,
                      fontweight="bold", color=TEXT, pad=8)

    # ── Scatter: Flow vs Pressure (coloured by anomaly type)
    ax_sc = fig.add_subplot(gs[1, 0])
    ds = df.sample(min(4000, len(df)), random_state=1)
    for atype, color in ANOMALY_COLORS.items():
        mask = ds["anomaly_type"] == atype
        if mask.any():
            ax_sc.scatter(ds["flow_lpm"][mask], ds["pressure_bar"][mask],
                          color=color, s=5, alpha=0.55, label=atype)
    style_ax(ax_sc, title="Flow vs Pressure", xlabel="Flow (L/min)", ylabel="Pressure (bar)")
    ax_sc.legend(fontsize=6.5, markerscale=2, framealpha=0.3, ncol=2)

    # ── Scatter: Soil Moisture vs Temperature
    ax_sc2 = fig.add_subplot(gs[1, 1])
    for atype, color in ANOMALY_COLORS.items():
        mask = ds["anomaly_type"] == atype
        if mask.any():
            ax_sc2.scatter(ds["temperature_c"][mask], ds["soil_moisture_pct"][mask],
                           color=color, s=5, alpha=0.55, label=atype)
    style_ax(ax_sc2, title="Temperature vs Soil Moisture",
             xlabel="Temperature (°C)", ylabel="Soil Moisture (%)")

    return fig


# PAGE 5 – Daily Patterns (hour-of-day aggregation)
def build_daily_patterns(df):
    fig = Figure(figsize=(14, 9), facecolor=BG, tight_layout=True)
    fig.suptitle("🕐  Daily Patterns (Hour-of-Day Averages)", fontsize=14,
                 fontweight="bold", color=TEXT, y=0.98)

    gs = gridspec.GridSpec(2, 3, figure=fig, hspace=0.55, wspace=0.40,
                           top=0.93, bottom=0.07, left=0.07, right=0.97)

    hours   = range(24)
    normal  = df[df["anomaly_type"] == "Normal"]
    hourly  = normal.groupby(normal.index.hour)

    sensors = [
        ("flow_lpm",          "Avg Flow (L/min)",     ACCENT),
        ("pressure_bar",      "Avg Pressure (bar)",   BLUE),
        ("soil_moisture_pct", "Soil Moisture (%)",    YELLOW),
        ("temperature_c",     "Temperature (°C)",     WARN),
        ("rain_probability",  "Rain Probability",     ACCENT2),
        ("evap_index",        "Evaporation Index",    "#f4a261"),
    ]

    for i, (col, ylabel, color) in enumerate(sensors):
        row, ci = divmod(i, 3)
        ax = fig.add_subplot(gs[row, ci])
        means  = hourly[col].mean()
        stds   = hourly[col].std()
        x      = means.index

        ax.fill_between(x, means - stds, means + stds, alpha=0.20, color=color)
        ax.plot(x, means, color=color, linewidth=2, marker="o", markersize=3)

        # mark irrigation windows
        ax.axvspan(6, 8,  alpha=0.08, color=ACCENT, zorder=0, label="Irrigation")
        ax.axvspan(17, 19, alpha=0.08, color=ACCENT, zorder=0)

        ax.set_xticks(range(0, 24, 3))
        ax.set_xticklabels([f"{h:02d}:00" for h in range(0, 24, 3)], fontsize=6.5)
        style_ax(ax, title=ylabel, xlabel="Hour of Day", ylabel=ylabel)

    return fig


# PAGE 6 – Normalized features (ML-ready view)
def build_normalized(df_norm):
    fig = Figure(figsize=(14, 9), facecolor=BG, tight_layout=True)
    fig.suptitle("⚙️  Normalized Features (ML-Ready, MinMax [0–1])", fontsize=14,
                 fontweight="bold", color=TEXT, y=0.98)

    feature_cols = [
        "flow_lpm", "pressure_bar", "soil_moisture_pct",
        "temperature_c", "rain_probability",
        "flow_rolling_mean", "flow_rolling_std", "pressure_drop",
        "flow_deviation", "evap_index",
    ]
    colors = [ACCENT, BLUE, YELLOW, WARN, ACCENT2,
              "#f4a261", "#e76f51", "#a8dadc", "#457b9d", "#2a9d8f"]

    gs = gridspec.GridSpec(2, 5, figure=fig, hspace=0.60, wspace=0.40,
                           top=0.93, bottom=0.07, left=0.06, right=0.97)

    normal  = df_norm[df_norm["anomaly_type"] == "Normal"]

    for i, (col, color) in enumerate(zip(feature_cols, colors)):
        row, ci = divmod(i, 5)
        ax = fig.add_subplot(gs[row, ci])
        vals = normal[col].dropna()
        ax.hist(vals, bins=35, color=color, alpha=0.80, edgecolor="none")
        ax.axvline(vals.mean(), color="white", linewidth=1.2, linestyle="--")
        label = col.replace("_", "\n")
        style_ax(ax, title=label, xlabel="Norm. Value", ylabel="Count")
        ax.tick_params(labelsize=7)
        mu, sd = vals.mean(), vals.std()
        ax.set_title(f"{label}\nμ={mu:.2f}  σ={sd:.2f}",
                     fontsize=8, color=TEXT, pad=4)

    return fig


