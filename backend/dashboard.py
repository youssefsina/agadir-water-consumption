class IrrigationDashboard(tk.Tk):
    def __init__(self, raw_df, norm_df):
        super().__init__()
        self.raw  = raw_df
        self.norm = norm_df
        self.title("💧  Smart Irrigation Data Dashboard")
        self.configure(bg=BG)
        self.geometry("1280x820")
        self.minsize(900, 600)

        self._pages = {}          # cache built figures
        self._current_canvas = None
        self._current_toolbar = None

        self._build_ui()
        self._show_page("Overview")

    # ── Layout ──────────────────────────────────
    def _build_ui(self):
        # ── Left sidebar
        sidebar = tk.Frame(self, bg=PANEL, width=180)
        sidebar.pack(side="left", fill="y")
        sidebar.pack_propagate(False)

        logo = tk.Label(sidebar, text="💧 IrriSense",
                        bg=PANEL, fg=ACCENT,
                        font=("DejaVu Sans", 13, "bold"), pady=18)
        logo.pack(fill="x")

        sep = tk.Frame(sidebar, bg=GRID, height=1)
        sep.pack(fill="x", padx=10, pady=4)

        pages = [
            ("📊  Overview",         "Overview"),
            ("🚨  Anomaly Timeline", "Anomaly Timeline"),
            ("📈  Distributions",    "Distributions"),
            ("🔗  Correlations",     "Correlations"),
            ("🕐  Daily Patterns",   "Daily Patterns"),
            ("⚙️   Normalized",       "Normalized"),
        ]

        self._btn_map = {}
        for label, key in pages:
            btn = tk.Button(
                sidebar, text=label, anchor="w",
                bg=PANEL, fg=TEXT,
                activebackground=ACCENT, activeforeground=BG,
                font=("DejaVu Sans", 9), padx=16, pady=10,
                bd=0, cursor="hand2", relief="flat",
                command=lambda k=key: self._show_page(k)
            )
            btn.pack(fill="x", padx=0, pady=1)
            self._btn_map[key] = btn

        # ── Info box at bottom of sidebar
        sep2 = tk.Frame(sidebar, bg=GRID, height=1)
        sep2.pack(fill="x", padx=10, pady=10, side="bottom")

        info = tk.Label(
            sidebar,
            text=f"Rows: {len(self.raw):,}\n"
                 f"Days: {(self.raw.index[-1]-self.raw.index[0]).days}\n"
                 f"Interval: 5 min\n"
                 f"Anomalies: {(self.raw['anomaly_type']!='Normal').sum():,}",
            bg=PANEL, fg=SUBTEXT, font=("DejaVu Sans", 8),
            justify="left", padx=14, pady=10
        )
        info.pack(fill="x", side="bottom")

        # ── Main content area
        self._content = tk.Frame(self, bg=BG)
        self._content.pack(side="right", fill="both", expand=True)

        # toolbar area
        self._toolbar_frame = tk.Frame(self._content, bg="#13161f", height=36)
        self._toolbar_frame.pack(fill="x", side="bottom")

        # canvas area
        self._canvas_frame = tk.Frame(self._content, bg=BG)
        self._canvas_frame.pack(fill="both", expand=True)

    def _show_page(self, key):
        # Highlight active button
        for k, btn in self._btn_map.items():
            btn.configure(bg=ACCENT if k == key else PANEL,
                          fg=BG   if k == key else TEXT)

        # Destroy previous canvas
        for w in self._canvas_frame.winfo_children():
            w.destroy()
        for w in self._toolbar_frame.winfo_children():
            w.destroy()

        # Build or retrieve cached figure
        if key not in self._pages:
            self.config(cursor="watch")
            self.update()
            builders = {
                "Overview":         lambda: build_overview(self.raw),
                "Anomaly Timeline": lambda: build_anomaly_timeline(self.raw),
                "Distributions":    lambda: build_distributions(self.raw),
                "Correlations":     lambda: build_correlations(self.raw),
                "Daily Patterns":   lambda: build_daily_patterns(self.raw),
                "Normalized":       lambda: build_normalized(self.norm),
            }
            self._pages[key] = builders[key]()
            self.config(cursor="")

        fig = self._pages[key]

        canvas = FigureCanvasTkAgg(fig, master=self._canvas_frame)
        canvas.draw()
        canvas.get_tk_widget().pack(fill="both", expand=True)

        toolbar = NavigationToolbar2Tk(canvas, self._toolbar_frame)
        toolbar.configure(bg="#13161f")
        toolbar.update()


