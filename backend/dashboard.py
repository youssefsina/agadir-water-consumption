SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))

def load_data():
    raw_path  = os.path.join(SCRIPT_DIR, "irrigation_raw.csv")
    norm_path = os.path.join(SCRIPT_DIR, "irrigation_normalized.csv")
    missing = [p for p in [raw_path, norm_path] if not os.path.exists(p)]
    if missing:
        messagebox.showerror(
            "Missing Files",
            "Could not find:\n" + "\n".join(missing) +
            "\n\nRun  generate_irrigation_data.py  first."
        )
        sys.exit(1)

    raw  = pd.read_csv(raw_path,  parse_dates=["timestamp"], index_col="timestamp")
    norm = pd.read_csv(norm_path, parse_dates=["timestamp"], index_col="timestamp")
    return raw, norm

